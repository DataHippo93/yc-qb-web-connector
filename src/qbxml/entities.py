"""
Entity definitions: maps internal entity names to qbXML query names,
sync order, and configuration.
"""
from __future__ import annotations

from dataclasses import dataclass
from typing import NamedTuple


@dataclass
class EntityDef:
    """Definition for a syncable QB entity."""
    name: str                    # Internal name (matches companies.yaml)
    query_rq: str                # qbXML request element name
    query_rs: str                # qbXML response element name
    ret_element: str             # The "Ret" element in response (e.g., CustomerRet)
    pk_field: str                # Primary key field: "ListID" or "TxnID"
    is_transaction: bool         # False = list object, True = transaction
    supports_iterator: bool      # Whether iterator is supported
    supports_modified_filter: bool  # Whether ModifiedDateRangeFilter works
    supports_txn_date_filter: bool  # Whether TxnDateRangeFilter works
    max_returned: int            # Default batch size
    sync_priority: int           # Lower = sync first


