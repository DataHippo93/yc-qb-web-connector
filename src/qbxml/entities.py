# Index by name for quick lookup
ENTITY_BY_NAME: dict[str, EntityDef] = {e.name: e for e in ENTITY_REGISTRY}


def get_entity(name: str) -> EntityDef:
    if name not in ENTITY_BY_NAME:
        raise KeyError(f"Unknown entity: {name}")
    return ENTITY_BY_NAME[name]


def get_entities_for_company(enabled_names: list[str]) -> list[EntityDef]:
    """Return EntityDef list in sync_priority order for enabled entities."""
    result = []
    for name in enabled_names:
        if name in ENTITY_BY_NAME:
            result.append(ENTITY_BY_NAME[name])
        # Silently skip unknown entity names (e.g., future additions)
    result.sort(key=lambda e: e.sync_priority)
    return result
